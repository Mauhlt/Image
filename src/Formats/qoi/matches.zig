const VEC_LEN = 64;
const VEC = @Vector(64, u8);

pub fn findFirst64Matches(comptime T: type, haystack: []const T, needle: T) usize {
    if (haystack.len >= 64) {
        const fields = @typeInfo(T).@"struct".fields;
        const n_fields = fields.len;
        const masks: [n_fields]VEC = comptime blk: {
            var masks: [n_fields]VEC = undefined;
            for (0..n_fields) |i| {
                masks[i] = std.simd.iota(u8, 64) * //
                    @as(VEC, @splat(n_fields)) + //
                    @as(VEC, @splat(@as(u8, @truncate(i))));
            }
            break :blk masks;
        };
        const base: @Vector(64 * n_fields, u8) = //
            @as(*const [64 * n_fields]u8, @ptrCast(haystack[0..64])).*;
        var haystacks: [n_fields]VEC = undefined;
        inline for (0..n_fields) |i| {
            haystacks[i] = @shuffle(u8, base, undefined, masks[i]);
        }
        var needles: [n_fields]VEC = undefined;
        inline for (0..n_fields) |i| {
            needles[i] = @splat(@field(needle, fields[i].name));
        }
        var matches: u64 = 0;
        for (0..n_fields) |i| {
            matches |= @bitCast(needles[i] != haystacks[i]);
        }
        return @ctz(matches);
    }
    for (haystack, 0..) |hay, i| {
        if (!hay.eql(needle)) return i;
    }
    return haystack.len;
}

